/**
 * Card Component
 * 
 * Glassmorphic card component matching the design
 * Creates frosted glass effect with backdrop blur
 */

import React from 'react';
import { cn } from '../lib/utils';

export interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'glass';
  padding?: 'none' | 'sm' | 'md' | 'lg';
}

export const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant = 'default', padding = 'md', children, ...props }, ref) => {
    return (
      <div
        ref={ref}
        className={cn(
          'rounded-3xl transition-all duration-200',
          {
            // Default variant
            'bg-white shadow-2xl': variant === 'default',

            // Glass variant (glassmorphic style)
            'bg-white/10 backdrop-blur-md border border-white/20 shadow-2xl':
              variant === 'glass',
          },
          {
            'p-0': padding === 'none',
            'p-4': padding === 'sm',
            'p-6': padding === 'md',
            'p-8': padding === 'lg',
          },
          className
        )}
        {...props}
      >
        {children}
      </div>
    );
  }
);

Card.displayName = 'Card';

